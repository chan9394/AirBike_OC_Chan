#import "PlanResult.h"

@implementation PlanResult

- (instancetype)initWithDistance:(NSInteger)dis andDuration:(NSInteger)dur andDestination:(NSString *)des {
    if (self = [super init]) {
        self.distance = dis;
        self.duration = dur;
        self.destination = des;
    }
    return self;
}

@end
